def _row_id(row: tuple) -> str:
    code, sl, period_days, gross = row[0], row[1], row[2], row[3]
    return f"{code}{'-SL' if sl else ''}-{period_days}d-${gross}"


@pytest.mark.parametrize("row", _GOLDEN, ids=[_row_id(r) for r in _GOLDEN])
def test_compute_paye_matches_golden_safety_net(row: tuple):
    """With the default config=SAFETY_NET the engine reproduces 2024/25 numbers.

    Asserts every monetary output line of compute_paye against the captured
    golden value for a representative battery of inputs. Any drift in the
    progressive brackets (M/ME), the IETC credit (ME), the secondary flat
    rates (SB/S/SH/ST/SA), the ACC levy/cap, or the student-loan
    rate/threshold breaks this test.
    """
    (
        tax_code,
        student_loan,
        period_days,
        gross,
        exp_annual,
        exp_paye,
        exp_ietc,
        exp_acc,
        exp_sl,
        exp_total_ird,
        exp_net,
    ) = row

    # No `config=` argument: exercises the default SAFETY_NET path.
    result = compute_paye(
        gross_pay=Decimal(gross),
        tax_code=tax_code,
        period_days=period_days,
        student_loan=student_loan,
    )

    assert result.annualised_gross == Decimal(exp_annual)
    assert result.paye_tax == Decimal(exp_paye)
    assert result.ietc_credit == Decimal(exp_ietc)
    assert result.acc_levy == Decimal(exp_acc)
    assert result.student_loan == Decimal(exp_sl)
    assert result.total_to_ird == Decimal(exp_total_ird)
    assert result.net_pay == Decimal(exp_net)


def test_explicit_safety_net_equals_default():
    """Passing config=SAFETY_NET explicitly equals omitting it entirely."""
    from app.modules.timesheets.paye import SAFETY_NET

    kwargs = dict(gross_pay=Decimal("2400"), tax_code="ME", period_days=14, student_loan=True)
    default = compute_paye(**kwargs)
    explicit = compute_paye(**kwargs, config=SAFETY_NET)

    assert default.paye_tax == explicit.paye_tax
    assert default.ietc_credit == explicit.ietc_credit
    assert default.acc_levy == explicit.acc_levy
    assert default.student_loan == explicit.student_loan
    assert default.net_pay == explicit.net_pay
